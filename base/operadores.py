
num1 = int(input("Digite um numero: "))
num2 = int(input("Digite outro numero: "))

# Operadores Aritméticos
print(f"Soma: {num1 + num2}")
print(f"Mult: {num1 * num2}")
print(f"Divisão: {num1 / num2}")
print(f"Subtração: {num1 - num2}")
print(f"mod: {num1 % num2}")
print(f"expoente: {num1 ** num2}")

# Operadores de Comparação
print(f"numero 1 é maior que numero 2: {num1 > num2}")
print(f"numero 1 é menor que numero 2: {num1 < num2}")
print(f"numero 1 é maior ou igual ao numero 2: {num1 >= num2}")
print(f"numero 1 é menor ou igual ao numero 2: {num1 <= num2}")

print(f"numero 1 é igual ao numero 2: {num1 == num2}")
print(f"numero 1 diferente do numero 2: {num1 != num2}")

# Operadores de atribuição
num1+=1
num1-=1
num1*=1
num1/=1



